# Numero = numero + 2

numero += 2 # Implica que le estamos SUMANDO el valor de la derecha a la variable de la izquierda.
numero *= 2 # Implica que le estamos MULTIPLICANDO el valor de la derecha a la variable de la izquierda.
numero -= 2 # Implica que le estamos RESTANDO el valor de la derecha a la variable de la izquierda.
numero /= 2 # Implica que le estamos DIVIDIENDO el valor de la derecha a la variable de la izquierda.

# Operaciones que podemos hacer con numeros: 

print(1 + 3)    # Suma
print(1 - 3)    # Resta
print(1 / 3)    # Division, Retorna el resultado de una division con decimales.
print(1 // 3)   # Cuando especificamos con dos slash estamos solicitando que nos retorne el valor de la division entero, que nos elimine los decimales o Float
print(1 * 3)    # Multriplicacion.
print(1 % 3)    # Modulo => Nos retorna el sobrante entero de la division de los dos numeros, ne este caso retornaria "1" porque es la cantidad de veces que esta 3 en 1 , o sea realiza una aproximacion.
print(2**3)     # Potenciacion, eleva un numero a x potencia.
