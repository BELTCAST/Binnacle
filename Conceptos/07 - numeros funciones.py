import math

# Cuando queremos realizar una aplicacion que tenga como referente el uso de matematicas, la libreria math nos ofrece gran variedad de metodos que nos facilitan el trabajo.

# Ejemplos de metodos fuera de math

print(round(1.3)) # Round, como su nombre nos indica, nos devolvera el valor aproximado o redondeado que le indiquemos 
print(abs(-77)) # Cn Abs tendremos la posibilidad de obtener numeros absolutos a partir de una entrada.

# Ejemplos de metodos que puede abarcar la libreria math

print(math.ceil(1.1))       # Toma un numero y lo aproxima al numero mas cercano hacia arriba.
print(math.floor(1.999))    # Toma el numero y lo aproxima al numero mas cercano hacia abajo.
print(math.isnan(23))       # Nos retorna es un boolean que nos confirma si es un numero/
print(math.pow(10,3))   	# Nos permite elevar un numero a una x potencia.
print(math.sqrt(9))         # Nos permite obtener la raiz cuadrada de un numero asignado 

