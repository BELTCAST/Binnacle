# Operadores ternarios:

# En Python, el operador ternario también se utiliza para evaluar expresiones condicionales y devolver valores basados en esa evaluación. 
# La sintaxis en Python es la siguiente:  valor_verdadero if condicion else valor si falso

edad = 15
mensaje = "Es mayor " if edad > 17 else "Es menor"

print(mensaje)


